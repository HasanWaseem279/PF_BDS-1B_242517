#include<stdio.h>
int main() {
	//Printing Characters
	char ch = "H";
	printf("printing Character: %c", ch);
	
	//Printing Decimal or Integer
	int x = 45, y = 90;
	printf("printing Decimal: %d", x);
	printf("printing Integer: %f", y);
	
	//Printing Float
	float f = 12.56;
	printf("printing Real: %f", f);
	
	printf("Print in Scientific Notation : %e\n");
	
	int a = 67;
	printf("Print in Ocatl Format: %o\n", a);
	
	printf("Print in Hex Format: %x\n", a);
	
	float z = 3.8;
	printf("float Value of y is : %g\n",z);
	
	
	
}